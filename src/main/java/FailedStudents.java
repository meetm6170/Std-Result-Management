    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        
        try{

            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection Con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","root");

            Statement st= Con.createStatement();
            ResultSet rs= st.executeQuery("select * from result inner join student on result.rollNo=student.rollNo");
           // ResultSet r= st.executeQuery("select *from student where student.rollNo='"+rollNo+"'");
            DefaultTableModel model = new DefaultTableModel(new String[]{"rollNo","name","mp","dbms","cn","tcs","msd"}, 0);
                    

           while(rs.next())
            {

                int rollNo = rs.getInt("rollNo");
               // String name= rs.getString("name");
               
               String name= rs.getString("name");
                int mp = rs.getInt("mp");
                int dbms = rs.getInt("dbms");
                int cn = rs.getInt("cn");
                int tcs = rs.getInt("tcs");
                int msd = rs.getInt("msd");
                
                int result= mp+dbms+cn+tcs+msd;
            
            if(mp<30 || dbms<30 || cn<30 || tcs<30 || msd<30)
            {

                model.addRow(new Object[]{rollNo,name,mp,dbms,cn,tcs,msd});
                
                // System.out.println("aaa");
            }
            }
            jTable1.setModel(model);
            Con.close();
        }
        catch(Exception e)
        {
            System.out.println(e);
            JOptionPane.showMessageDialog(null,"Connection Error");
        }
        jTable1.setEnabled(false);
        
    }//GEN-LAST:event_formComponentShown


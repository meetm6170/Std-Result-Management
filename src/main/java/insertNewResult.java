    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollNo=jTextField1.getText();
        String mp=jTextField2.getText();
        String dbms=jTextField3.getText();
        String cn=jTextField4.getText();
        String tcs=jTextField5.getText();
        String msd=jTextField6.getText();
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection Con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","root");
            Statement st= Con.createStatement();
            ResultSet rs= st.executeQuery("select *from student where rollNo='"+rollNo+"'");
            if(rs.next())
            {
                st.executeUpdate("insert into result (rollNo,mp,dbms,cn,tcs,msd) values('"+rollNo+"', '"+mp+"', '"+dbms+"', '"+cn+"', '"+tcs+"', '"+msd+"')");
                JOptionPane.showMessageDialog(null,"Successfully Updated");
                setVisible(false);
                new insertNewResult().setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"RollNo does not exist in our database");
                setVisible(false);
                new insertNewResult().setVisible(true);   
            }
         
        }
        catch (Exception e)
        {
           JOptionPane.showMessageDialog(null,"Connection Error");
            
        }
    }//GEN-LAST:event_jButton6ActionPerformed


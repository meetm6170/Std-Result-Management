    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
          try{

            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection Con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","root");

            Statement st= Con.createStatement();
            ResultSet rs= st.executeQuery("select *from student");

            DefaultTableModel model = new DefaultTableModel(new String[]{"rollNo","course","branch","name","gender","fatherName"}, 0);

            while(rs.next())
            {

                int c = rs.getInt("rollNo");

                String d = rs.getString("course");
                String e = rs.getString("branch");
                String f= rs.getString("name");
               String g = rs.getString("gender");
                String h = rs.getString("fatherName");

                model.addRow(new Object[]{c,d,e,f,g,h});
                // System.out.println("aaa");
            }
            jTable2.setModel(model);
            Con.close();
        }
        catch(Exception e)
        {
            System.out.println(e);
            JOptionPane.showMessageDialog(null,"Connection Error");
        }
        jTable2.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

